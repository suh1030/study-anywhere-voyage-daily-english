def fix_file(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    original = content
    changes = []

    # Step 1: Replace def: '本行關鍵詞' with proper definitions
    def replace_def(m):
        word = m.group(1)
        if word in VOCAB_MAP:
            definition = VOCAB_MAP[word]
            changes.append(f"  vocab def fixed: '{word}' → '{definition}'")
            return f"word: '{word}', def: '{definition}'"
        else:
            # Fallback: use the word itself as context
            changes.append(f"  vocab def (fallback): '{word}'")
            return f"word: '{word}', def: '（{word}相關表達）'"

    content = re.sub(
        r"word: '([^']+)', def: '本行關鍵詞'",
        replace_def,
        content
    )

    # Step 2: Fix broken apostrophe en-text embeddings
    for broken, fixed in BROKEN_EN_FIXES.items():
        if broken in content:
            count = content.count(broken)
            content = content.replace(broken, fixed)
            changes.append(f"  en-text fixed: '{broken.strip()}' → '{fixed.strip()}' ({count}×)")

    if content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        return True, changes
    return False, []


def main():
    files = sorted(glob.glob(os.path.join(BASE, 'content/episodes/week-*.ts')))
    total_files = 0
    total_changes = 0

    print(f"Processing {len(files)} week files...\n")

    for filepath in files:
        fname = os.path.basename(filepath)
        changed, changes = fix_file(filepath)
        if changed:
            total_files += 1
            total_changes += len(changes)
            print(f"✓ {fname} ({len(changes)} changes)")

    print(f"\n{'='*50}")
    print(f"Total files modified: {total_files}")
    print(f"Total changes applied: {total_changes}")


if __name__ == '__main__':
    main()
